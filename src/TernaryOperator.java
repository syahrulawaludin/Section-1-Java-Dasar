public class TernaryOperator {
    

    public static void main(String[] args) {
        
        // Ternary Operator

        // Ternary operator adalah operator sederhana dari if statement
        // Ternary operatpr terdiri dari kondisi yang dievaluasi, jika menghasilkan true maka nilai pertama diambil, jika false, maka nilai kedua diambil
        

        var nilai = 75;

        String  ucapan = nilai >= 75 ? "Selanat anda Lulus" : "Silahkan coba lagi";
        System.out.println(ucapan);


   
    }
}
