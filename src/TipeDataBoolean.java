public class TipeDataBoolean {
    
    public static void main(String[] args) {
        
        // Tipe data Boolean

        // 1. Tipe data boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah
        // 2. Tipe data boolean di java dipresentasikan dengan kata  kunci boolean
        // 3. Nilai benar dipresentasikan dengan kata kunci true
        // 4. Nilai  salah dipresentasikan dengan kata kunci false
        
        // 5. Default value  untuk boolean adalah false

        boolean benar = true;
        boolean salah = false;


        System.out.println(benar);
        System.out.println(salah);



    }
}
